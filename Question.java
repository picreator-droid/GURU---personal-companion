public class Question {
    private int questionId;
    private String contentHindi;
    private String contentEnglish;
    private String category;

    // Getters and setters
    public int getQuestionId() {
        return questionId;
    }
    public void setQuestionId(int questionId) {
        this.questionId = questionId;
    }
    public String getContentHindi() {
        return contentHindi;
    }
    public void setContentHindi(String contentHindi) {
        this.contentHindi = contentHindi;
    }
    public String getContentEnglish() {
        return contentEnglish;
    }
    public void setContentEnglish(String contentEnglish) {
        this.contentEnglish = contentEnglish;
    }
    public String getCategory() {
        return category;
    }
    public void setCategory(String category) {
        this.category = category;
    }
}
